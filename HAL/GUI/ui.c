/*-----------------------------------------------------------------------------
Function Name	:	_CMIOT_ShowRadioInfo
Author			:	zhaoji
Created Time	:	2018.03.14
Description 	: 	获取模组射频信息、信息信息展示到LCD屏幕
Input Argv		:
Output Argv 	:
Return Value	:
-----------------------------------------------------------------------------*/
void _CMIOT_ShowRadioInfo()
{
	CMIOT_UE_State ue_state;
	uint8_t CsqValue = 0;
	uint8_t t3412[32] = {0};
	uint8_t t3324[32] = {0};
	
	/* 显示参数名 */
	POINT_COLOR = BLACK;
	LCD_ShowString(75, 50, 16,  (u8 *)"CSQ    :");
	LCD_ShowString(75, 75, 16,  (u8 *)"SNR    :");
	LCD_ShowString(75, 100, 16, (u8 *)"RSRQ   :");
	LCD_ShowString(75, 125, 16, (u8 *)"EARFCN :");
	LCD_ShowString(75, 150, 16, (u8 *)"ECL    :");
	LCD_ShowString(75, 175, 16, (u8 *)"PLMN   :");
	LCD_ShowString(75, 200, 16, (u8 *)"BAND   :");
	LCD_ShowString(75, 225, 16, (u8 *)"T3324  :");
	LCD_ShowString(75, 250, 16, (u8 *)"T3412  :");
	LCD_ShowString(75, 275, 16, (u8 *)"CELLID :");
	
	while(1)
	{
		/* 刷新信号强度 */
		CsqValue = _CMIOT_M5310_GetSignalstrength();
		sprintf((char *)msg, "%d", CsqValue);
		LCD_Fill(155, 50, 239, 70, WHITE);
		LCD_ShowString(155, 50, 16,  (u8 *)msg);
		
		/* 刷新NUESTATS参数 */
		/* SNR */
		ue_state = _CMIOT_M5310_GetUeState();
		sprintf((char *)msg, "%d", ue_state.snr);
		LCD_Fill(155, 75, 239, 95, WHITE);
		LCD_ShowString(155, 75, 16,  (u8 *)msg);
		/* RSRQ */
		sprintf((char *)msg, "%d", ue_state.rsrq);
		LCD_Fill(155, 100, 239, 120, WHITE);
		LCD_ShowString(155, 100, 16,  (u8 *)msg);
		/* EARFCN */
		sprintf((char *)msg, "%d", ue_state.earfcn);
		LCD_Fill(155, 125, 239, 150, WHITE);
		LCD_ShowString(155, 125, 16,  (u8 *)msg);
		/* ECL */
		sprintf((char *)msg, "%d", ue_state.ecl);
		LCD_Fill(155, 150, 239, 170, WHITE);
		LCD_ShowString(155, 150, 16,  (u8 *)msg);
		/* CELLID */
		LCD_Fill(155, 275, 239, 295, WHITE);
		LCD_ShowString(155, 275, 16,  ue_state.cellid);
		
		/* 刷新PLMN号 */
		_CMIOT_GetPLMN(msg, sizeof(msg));
		LCD_Fill(155, 175, 239, 195, WHITE);
		LCD_ShowString(155, 175, 16,  msg);
		
		/* 刷新BAND频段号 */
		sprintf((char *)msg, "%d", _CMIOT_GetNB_Band());
		LCD_ShowString(155, 200, 16, msg);
		
		/* 刷新T3412 T3324定时器值 */
		_CMIOT_Get_PSM_TIMER_Value(t3324, t3412, sizeof(t3324));
		LCD_Fill(155, 225, 239, 245, WHITE);
		LCD_ShowString(155, 225, 16,  t3324);
		
		LCD_Fill(155, 250, 239, 270, WHITE);
		LCD_ShowString(155, 250, 16,  t3412);
	}
}


/*-----------------------------------------------------------------------------
Function Name	:	_CMIOT_ShowNetworkRegTime
Author			:	zhaoji
Created Time	:	2018.03.16
Description 	: 	测试驻网时间，并展示到LCD屏幕
Input Argv		:
Output Argv 	:
Return Value	:
-----------------------------------------------------------------------------*/
void _CMIOT_ShowNetworkRegTime()
{
	uint32_t TestCount = 0;
	uint32_t FailCount = 0;
	uint32_t MaxRegisterTime = 0;
	uint32_t AvrRegisterTime = 0;
	uint32_t MinRegisterTime = 0;
	uint32_t result;
	
	LCD_ShowChinese(105, 55, Arial, 24, (u8 *)"驻网时间", LIGHTBLUE, WHITE);
	LCD_ShowFontEN(95, 85, (u8 *)"---.---", 24, BRRED, WHITE);
	LCD_ShowChinese(80, 130, newArial, 16, (u8 *)"测试次数", BLACK, WHITE);
	POINT_COLOR = BLACK;
	LCD_ShowString(150, 130, 16,  (u8 *)":");
	LCD_ShowChinese(80, 155, newArial, 16, (u8 *)"失败次数", BLACK, WHITE);
	POINT_COLOR = BLACK;
	LCD_ShowString(150, 155, 16,  (u8 *)":");
	LCD_ShowChinese(80, 180, newArial, 16, (u8 *)"成功率", BLACK, WHITE);
	POINT_COLOR = BLACK;
	LCD_ShowString(150, 180, 16,  (u8 *)":");
	LCD_ShowChinese(80, 205, newArial, 16, (u8 *)"平均时长", BLACK, WHITE);
	POINT_COLOR = BLACK;
	LCD_ShowString(150, 205, 16,  (u8 *)":");
	LCD_ShowChinese(80, 230, newArial, 16, (u8 *)"最大时长", BLACK, WHITE);
	POINT_COLOR = BLACK;
	LCD_ShowString(150, 230, 16,  (u8 *)":");
	LCD_ShowChinese(80, 255, newArial, 16, (u8 *)"最短时长", BLACK, WHITE);
	POINT_COLOR = BLACK;
	LCD_ShowString(150, 255, 16,  (u8 *)":");
	while(1)
	{
		result = _CMIOT_M5310_GetRegisterTime();
		// LCD_Fill(95, 90, 239, 120, WHITE);
		delay_ms(50);
		
		if(result > 1)
		{
			TestCount++;	/* 测试次数+1 */
			sprintf((char *)msg, "%07.3fs", (float)result/1000);
			LCD_ShowFontEN(95, 85, (u8 *)msg, 24, BRRED, WHITE);
		}
		else if(result == 1)
		{
			LCD_ShowFontEN(95, 90, (u8 *)"timeout", 24, RED, WHITE);
			TestCount++;	/* 测试次数+1 */
			FailCount++;	/* 失败次数+1 */
		}
		else
		{
			LCD_ShowFontEN(95, 90, (u8 *)"booterr", 24, RED, WHITE);
		}
		POINT_COLOR = BLACK;
		/* 显示测试次数 */
		sprintf((char *)msg, "%d", TestCount);
		LCD_Fill(165, 130, 239, 150, WHITE);
		LCD_ShowString(165, 130, 16,  msg);
		/* 显示失败次数 */
		sprintf((char *)msg, "%d", FailCount);
		LCD_Fill(165, 155, 239, 150, WHITE);
		LCD_ShowString(165, 155, 16,  msg);
		/* 显示成功率 */
		if(TestCount > 0)
		{
			sprintf((char *)msg, "%.2f%%", (float)((TestCount - FailCount)/TestCount)*100);
			LCD_Fill(165, 180, 239, 150, WHITE);
			LCD_ShowString(165, 180, 16,  msg);
		}
		/* 平均时长 */
		if(result > 1)
		{
			AvrRegisterTime = (AvrRegisterTime * (TestCount - FailCount - 1) + result)/(TestCount - FailCount);
			sprintf((char *)msg, "%07.3fs", (float)AvrRegisterTime/1000);
			LCD_Fill(165, 205, 239, 150, WHITE);
			LCD_ShowString(165, 205, 16,  msg);
		}
		/* 最大时长 */
		if(result > MaxRegisterTime && result > 1) { MaxRegisterTime = result; }
		sprintf((char *)msg, "%07.3fs", (float)MaxRegisterTime/1000);
		LCD_Fill(165, 230, 239, 150, WHITE);
		LCD_ShowString(165, 230, 16,  msg);
		/* 最短时长 */
		if(((result < MinRegisterTime) || (MinRegisterTime == 0)) && (result > 1)) { MinRegisterTime = result; }
		sprintf((char *)msg, "%07.3fs", (float)MinRegisterTime/1000);
		LCD_Fill(165, 255, 239, 150, WHITE);
		LCD_ShowString(165, 255, 16,  msg);
	}
}


